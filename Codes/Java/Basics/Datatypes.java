package Java;

import java.io.*;

public class Datatypes {

    public static void main(String args[]) {

        // Datatypes in Java
        // Primitive Datatypes

        char a = 'A';
        byte b = 2;
        short c = 22;
        int d = 45;
        float e = 23;
        double f = 45.67;
        boolean g = true;

        // Non Primitive Types

        String str = "Rohit Maity";
        int arr[] = new int[4];

        /*
         * Now all this datatypes have their specific sizes as per compilers.
         */

        System.out.println("The value of char is");
        System.out.println(a);

    }

}
